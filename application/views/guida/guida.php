    <fieldset>
        <legend>Meter</legend>
        La sezione Meter permette di visualizzare previa attivazione della connessione alla rete e del gps,
        visualizzare, in tempo reale, la qualità dell'ambiente circostante al punto dove ci si trova.
    </fieldset>
    <br>
    <fieldset>
        <legend>Mappa</legend>
        La sezione Mappa permette di visualizzare tutte le strutture resistrate.
        Le strutture vengono visualizzate in base alla loro posizione reale e il colore dell'icona varia colore in base alla qualità dell'ambiente
        <br><br>
        Cliccando sulle varie strutture si potranno visualizzare le informazioni relativa ad una struttura.
        Verrà indicato il nome della struttura, la valutazione in stelle ed il punteggio numerico.
        Si potrà vedere quanto recenti sono i dati utilizzati per il calcolo del punteggio.
        Sarà consultabile anche un grafico indicante l'andamento nel tempo della qualità nei pressi della struttura selezionata.
        Inoltre cliccando sull'apposito pulsante si potrà essere reindirizzati al sito web ufficiale della struttura.
    </fieldset>
    <br>
    <fieldset>
        <legend>Calcolo dell'indice</legend>
        Nella sezione relativa al calcolo dell'indice verrà indicato come viene calcolato l'indice in base agli open data a disposizione.
    </fieldset>
    <br>
    <fieldset>
        <legend>Licenze</legend>
        La sezione Licenze permette di visualizzare le varie licenze degli open data utilizzati e la licenza sotto la quale è distribuita l'applicazione.
    </fieldset>
    <br>
    <fieldset>
        <legend>Sito web</legend>
        La voce del menu relativa al sito web permette di collegarsi direttamente al sito ufficiale dell'applicazione.
    </fieldset>
